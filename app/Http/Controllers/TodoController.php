    {
        $todo = [
            'title' => $request->title,
            'description' => $request->description,
            
        ];
        $to = Todo::create($todo);

        if(!empty($to)){
            return redirect()->route('todo.index');
        }
        else{
            return redirect()->back();
        }
    }

